        <figure>
            <h1>Uitspraak in klimaatzaak: moet de overheid meer doen tegen CO2-uitstoot?</h1>
            <p class="text-bol"><i class="fal fa-clock"></i> VANDAAG, 8:55</p>
            <img src="https://nos.nl/data/image/2018/10/09/505245/xxl.jpg">
        </figure>
        <p>Vandaag doet het gerechtshof in Den Haag uitspraak in de klimaat-zaak van actiegroep Urgenda. Vraag is of de overheid meer moet doen om de uitstoot van CO2 terug te dringen. De rechter bepaalde drie jaar geleden in deze zaak dat de Staat de CO2-uitstoot in 2020 met 25 procent moet verlagen, ten opzichte van 1990. De zaak werd historisch genoemd en leidde tot flink wat discussie.</p>
        <p>De rechter ging met de uitspraak te veel op de stoel van de overheid zitten, vond de Staat, die in hoger beroep ging. Niet omdat klimaatveranderingen niet serieus worden genomen, benadrukte de advocaat meerdere malen tijdens zijn pleidooi. "Maar omdat de rechtbank een beleidsmatige en politieke keuze heeft gemaakt." De rechter hoort dat volgens hem niet te doen. Tegelijk beloofde het kabinet het vonnis wel te zullen uitvoeren.</p>
        <h3>Leefbaarheid voor (klein)kinderen</h3>
        <p>Dat is ook nodig, zegt Sietse Brouwer van Grootouders voor het Klimaat. "Als het zo doorgaat komt de voedselvoorziening in het geding door droogte en wateroverlast." Samen met andere grootouders maakt hij zich net als Urgenda sterk voor leefbaarheid van de wereld voor zijn kleinkinderen. "Daarom staan wij twee keer per maand bij de Tweede Kamer. Daar praten we met Kamerleden en bewindspersonen om hen aan te sporen om spoed te maken met goed klimaatbeleid, want dat is echt heel hard nodig."</p>
        <p>Hij vindt dat die boodschap in Den Haag nog niet goed genoeg aankomt. "En hoe sneller we ingrijpen, hoe beperkter de gevolgen zullen zijn. De politiek moet dus sneller acteren." Over de snelheid waarmee dat nu gebeurt, of het gebrek daaraan, maakt hij zich zorgen.</p>
        <p>"Maar ik ben ook optimistisch. Want er zijn oplossingen voorhanden: bedrijven moeten acteren, de politiek moet de wettelijke kaders plaatsen en ook de consument moet aan de slag." Dat betekent volgens Brouwer niet dat het leven er minder leuk op wordt, vertelt hij in het NOS Radio 1 Journaal. "Dat kan echt op een hele leuke manier. Je hoeft jezelf ook niet alles te ontzeggen, maar hoe sneller we gedragsverandering doorzetten, des te leuker het blijft."</p>
        <h3>Uitdaging</h3>
        <p>Op dit moment staat het kabinet voor een flinke uitdaging. Het CBS maakte een maand geleden bekend dat de totale uitstoot aan broeikasgassen in 2017 slechts 13 procent lager was dan in 1990. Volgens het vonnis van drie jaar geleden moet de uitstoot in 2020 een kwart lager zijn. Als het hof de eerdere uitspraak verwerpt hoeft de Staat dit doel niet wettelijk na te komen.</p>
