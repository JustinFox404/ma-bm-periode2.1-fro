        <p>In Nieuwegein, Utrecht, Tiel en Vianen heeft de politie vanmorgen op acht plekken invallen gedaan. Zeven mensen zijn daarbij gearresteerd. Op dit moment vinden nog verschillende doorzoekingen plaats.</p>
        <p>De politie wil alleen zeggen dat de invallen te maken hebben met "een lopend onderzoek".</p>
        <p>Volgens justitie-redacteur Remco Andringa heeft de operatie te maken met een vete in de onderwereld, die de afgelopen jaren heeft geleid tot een lange reeks liquidaties. De politie houdt daarom zoekacties in verschillende woningen en opslagplaatsen. "Daar zijn ze op zoek naar zaken die bij liquidaties worden gebruikt, zoals bijvoorbeeld snelle auto's en zware wapens", zegt Andringa.</p>
        <p>Het onderzoek houdt ook verband met Ridouan T, die ervan wordt verdacht als opdrachtgever betrokken te zijn geweest bij verschillende liquidaties. "Er wordt zelfs rekening gehouden met meer dan tien moorden", aldus Andringa.</p>
        <p>"Iemand uit de kring van T. is een tijdje geleden overgelopen naar de politie en heeft daar een boekje open gedaan over onder meer de activiteiten van T. Vlak daarna werd de onschuldige broer van deze overgelopen man doodgeschoten, kennelijk als vergelding", aldus Andringa. "Dat geeft wel aan dat het gaat om zeer zware criminelen die niet terugdeinzen voor een moord meer of minder."</p>

        <p>T. is zelf is vanmorgen niet aangehouden. Bronnen melden dat er wel invallen zijn gedaan bij familie van hem. T. is al maanden spoorloos en wordt internationaal gezocht. Voor zijn aanhouding is een beloning uitgeloofd van 25.000 euro.</p>
        <p>Ridouan T. groeide op in Vianen en is volgens het OM actief in "een crimineel milieu waarin heel snel wordt overgegaan tot geweld". Het OM heeft niet bekendgemaakt aan welke liquidaties T. wordt gelinkt. Op misdaadsites wordt zijn naam in verband gebracht met moorden die werden uitgevoerd door een groep criminelen uit Nieuwegein.</p>
